public class Equipment {

    String name;
    int upkeepValue;
    int value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getUpkeepValue() {
        return upkeepValue;
    }

    public void setUpkeepValue(int upkeepValue) {
        this.upkeepValue = upkeepValue;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
